#include <unistd.h>

void	ft_is_negative(int n)
{
	char	p;
	char	l;

	p = 'P';
	l = 'N';
	if (n >= 0)
	{
		write(1, &p, 1);
	}
	else
	{
		write(1, &l, 1);
	}
}
